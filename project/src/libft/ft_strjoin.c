#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char		*result;
	size_t		r_len;
	size_t		i;
	size_t		j;

	i = 0;
	j = 0;
	r_len = ft_strlen(s1) + ft_strlen(s2);
	result = (char *)malloc(r_len + 1);
	if (!result)
		return (NULL);
	while (s1[i])
	{
		result[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		result[i + j] = s2[j];
		j++;
	}
	result[i + j] = '\0';
	return (result);
}
